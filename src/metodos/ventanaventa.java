    public void mostrar(){
    int i,j;
    String num;
    for(i=0;i<m1.getnv();i++)
    {
    for(j=0;j<m1.getnm();j++)
    {
    num=String.valueOf(m1.getventas(i, j));
    mat.setValueAt(num, i, j);
   
    }
    
    }
    
 
    }
    
    
    
    
    private void BLLENARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BLLENARActionPerformed
      int fila, columna;
      double vent=0;
      fila=Integer.parseInt(txtnvendedores.getText());
      columna=Integer.parseInt(TXTNUMESES.getText());
      m1.setnv(fila);
      m1.setnm(columna);
    DefaultTableModel Md = (DefaultTableModel)mat.getModel();
    Md.setRowCount(fila);
    Md.setColumnCount(columna);
    m1.setnv(mat.getModel().getRowCount());
    m1.setnm(mat.getModel().getColumnCount());
      m1.llenar();
     
    }//GEN-LAST:event_BLLENARActionPerformed

